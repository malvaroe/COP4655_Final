package com.example.hw8_5;

public class Day {
    public int day;
    public double temp;

    public Day(int day, double temp){
        this.day = day;
        this.temp = temp;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public double getTemp() {
        return temp;
    }

    public void setTemp(double temp) {
        this.temp = temp;
    }
}
